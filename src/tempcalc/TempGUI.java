    @Override
    public void actionPerformed(ActionEvent ae) {
        Object source = ae.getSource();
        if (source == CText){ convertToCelsius(); }
        if (source == CButton){ convertToCelsius(); }
        if (source == FText){ convertToFahrenheit(); }
        if (source == FButton){ convertToFahrenheit(); }
    }
    
    public void convertToCelsius(){
        CResult.setText("Result: " + TempCalculator.celsiusConvert(CText.getText()) + "째C");
    }
    
    public void convertToFahrenheit (){
        FResult.setText("Result: " + TempCalculator.fahrenheitConvert(FText.getText()) + "째F");
    }
